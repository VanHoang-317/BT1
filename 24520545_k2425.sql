CREATE DATABASE QLKVC

USE QLKVC
 DROP TABLE KHU_VUI_CHOI
CREATE TABLE KHU_VUI_CHOI (
	MaKVC			VARCHAR(10) PRIMARY KEY,
	TenKVC			VARCHAR(50),
	DiaChi			VARCHAR(100),
	GioMoCua		TIME,
	GioDongCua		TIME,
	TrangThai		VARCHAR(10)
)

CREATE TABLE KHU_VUC (
	MaKhuVuc		VARCHAR(10) PRIMARY KEY,
	TenKhuVuc		VARCHAR(50),
	MoTa			VARCHAR(100),
	SucChuaToiDa	INT,
	MaKVC			VARCHAR(10)
)

CREATE TABLE VE (
	MaVe			VARCHAR(10),
	MaKhuVuc		VARCHAR(10),
	TenVe			VARCHAR(50),
	LoaiVe			VARCHAR(20),
	GiaCoBan		INT,
	HieuLucTu		TIME,
	HieuLucDen		TIME,
	PRIMARY KEY(MaVe, MaKhuVuc)
)

CREATE TABLE KHACH_HANG (
	MaKH			VARCHAR(10) PRIMARY KEY,
	HoTen			VARCHAR(50),
	SDT				VARCHAR(20),
	Email			VARCHAR(50),
	NgaySinh		SMALLDATETIME,
	NgayTao			SMALLDATETIME,
	LoaiKH			VARCHAR(10)
)

CREATE TABLE DON_MUA (
	MaDon			VARCHAR(10) PRIMARY KEY,
	MaKH			VARCHAR(10),
	NgayMua			SMALLDATETIME,
	TongTien		INT,
	PhuongThucTT	VARCHAR(20),
	TrangThai		VARCHAR(10)
)

CREATE TABLE CT_DON_MUA (
	MaDon			VARCHAR(10),
	MaVe			VARCHAR(10),
	SoLuong			INT,
	DonGia			INT,
	NgaySuDung		SMALLDATETIME,
	MaQR			VARCHAR(10),
	PRIMARY KEY(MaDon, MaVe)
)
ALTER TABLE KHU_VUC
ADD CONSTRAINT FK_MAKHUVUC_KVC FOREIGN KEY(MAKVC) REFERENCES KHU_VUI_CHOI(MAKVC)
ALTER TABLE VE 
ADD CONSTRAINT FK_MAKHUVUC_VE FOREIGN KEY(MAKHUVUC) REFERENCES KHU_VUC(MAKHUVUC)
ALTER TABLE DON_MUA 
ADD CONSTRAINT FK_MAKH_DM FOREIGN KEY(MAKH) REFERENCES KHACH_HANG(MAKH)
ALTER TABLE CT_DON_MUA 
ADD CONSTRAINT FK_MADON_CT FOREIGN KEY(MADON) REFERENCES DON_MUA(MADON)
ALTER TABLE CT_DON_MUA 
ADD CONSTRAINT FK_MAVE_CT FOREIGN KEY(MAVE) REFERENCES VE(MAVE)

