SELECT MADON,NGAYMUA,TONGTIEN
FROM DON_MUA  
WHERE MAKH ='KH01'
GROUP BY MADON,NGAYMUA,TONGTIEN
ORDER BY NGAYMUA DESC
--2.
SELECT KV.MAKHUVUC,TENKHUVUC,SUM(SOLUONG) AS TONGSOVE
FROM CT_DON_MUA  CT
JOIN VE 
ON VE.MAVE =  CT.MAVE
JOIN KHU_VUC  KV
ON VE.MAKHUVUC = KV.MAKHUVUC
GROUP BY KV.MAKHUVUC,TENKHUVUC 
--3.
SELECT KH.MAKH,HOTEN,COUNT(VE.MAVE) AS SOVE 
FROM KHACH_HANG KH 
JOIN DON_MUA DM 
ON DM.MAKH = KH.MAKH 
JOIN CT_DON_MUA CT 
ON CT.MADON = DM.MADON 
JOIN VE 
ON VE.MAVE = CT.MAVE 
GROUP BY KH.MAKH,HOTEN
HAVING COUNT(VE.MAVE) = (SELECT COUNT(V1.MAKHUVUC)
							FROM CT_DON_MUA CT1
							JOIN VE V1 ON V1.MAVE = CT1.MAVE